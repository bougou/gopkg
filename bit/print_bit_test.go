package bit

import (
	"testing"
)

func Test_FormatByteSlice(t *testing.T) {
	data := []byte{
		0x6, 0x0, 0xff, 0x7, 0x6, 0xc0, 0xd4, 0x49,
		0xab, 0xfd, 0x4, 0x0, 0x0, 0x0, 0x20, 0x0,
		0x95, 0x66, 0xc7, 0x4d, 0x10, 0x3, 0x7c, 0x4d,
		0x7b, 0xbb, 0x4, 0x7, 0xd1, 0xe2, 0xc6, 0x49,
		0x30, 0x33, 0x35, 0xff, 0x1e, 0x64, 0x48, 0xe5,
		0x4, 0x75, 0x5f, 0x3b, 0x1, 0xe3, 0x3a, 0x6f,
		0xff, 0xff, 0x2, 0x7, 0x8a, 0xdd, 0xc, 0x9a,
		0xde, 0xd6, 0x9, 0x53, 0x98, 0xa1, 0x6b, 0x16,
	}

	output := `06 00 ff 07 06 c0 d4 49
ab fd 04 00 00 00 20 00
95 66 c7 4d 10 03 7c 4d
7b bb 04 07 d1 e2 c6 49
30 33 35 ff 1e 64 48 e5
04 75 5f 3b 01 e3 3a 6f
ff ff 02 07 8a dd 0c 9a
de d6 09 53 98 a1 6b 16
`
	if FormatByteSlice(data, 8) != output {
		t.Error("FormatByteSlice returned result does not match")
	}

}
